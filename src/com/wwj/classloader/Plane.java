package com.wwj.classloader;

public class Plane {
	
	private String name;
	
	private int planeHeight;
	
	private long planeDistance;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPlaneHeight() {
		return planeHeight;
	}

	public void setPlaneHeight(int planeHeight) {
		this.planeHeight = planeHeight;
	}

	public long getPlaneDistance() {
		return planeDistance;
	}

	public void setPlaneDistance(long planeDistance) {
		this.planeDistance = planeDistance;
	}
	

}
